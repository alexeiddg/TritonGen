"""Modal entry point for evaluating external baseline JSONL files.

Runs Triton JIT compilation on kernels generated by run_external_baselines.py
via the shared Modal GPU harness (L4 GPU, same gate as Cluster 1).

Usage:
    modal run -m eval_external_modal
    modal run -m eval_external_modal --force        # re-evaluate already-done rows
    modal run -m eval_external_modal --model claude # only one model
"""

from __future__ import annotations

import json
import uuid
from pathlib import Path

# These imports must be at module level so Modal hydrates the remote function
# before app.run() is entered — same contract as run_cluster1_modal.py.
from shared.modal_harness.app import app
from shared.modal_harness.compile import remote_compile_only  # noqa: F401 — hydration
from cluster1.validation.modal_compile_check import check_compiles_modal

EXTERNAL_DIR = Path("outputs/external")
FILES = {
    "claude": EXTERNAL_DIR / "claude_baseline_n20.jsonl",
    "gemini": EXTERNAL_DIR / "gemini_baseline_n20.jsonl",
    "openai": EXTERNAL_DIR / "openai_baseline_n20.jsonl",
}


def _load(path: Path) -> list[dict]:
    rows = []
    with open(path, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                rows.append(json.loads(line))
    return rows


def _write(path: Path, rows: list[dict]) -> None:
    tmp = path.with_suffix(".jsonl.tmp")
    with open(tmp, "w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")
    tmp.replace(path)


def _print_summary(all_rows: dict[str, list[dict]]) -> None:
    kernel_classes = ["elementwise", "matmul", "reduction"]
    dtypes = ["fp32", "fp16", "bf16"]
    print("\n" + "=" * 72)
    print("COMPILE SUCCESS SUMMARY")
    print("=" * 72)
    for condition, rows in all_rows.items():
        ev = [r for r in rows if r.get("compile_success") is not None]
        ok = sum(1 for r in ev if r.get("compile_success") is True)
        pct = f"{ok/len(ev)*100:.1f}%" if ev else "N/A"
        print(f"\n  {condition}  (n={len(rows)}, evaluated={len(ev)})")
        print(f"  Overall: {ok}/{len(ev)}  {pct}")
        print("  By kernel_class:")
        for kc in kernel_classes:
            sub = [r for r in ev if r.get("kernel_class") == kc]
            n_ok = sum(1 for r in sub if r.get("compile_success") is True)
            rate = f"{n_ok/len(sub)*100:.1f}%" if sub else "N/A"
            print(f"    {kc:15s}  n={len(sub)}  ok={n_ok}  rate={rate}")
        print("  By dtype:")
        for dt in dtypes:
            sub = [r for r in ev if r.get("dtype") == dt]
            n_ok = sum(1 for r in sub if r.get("compile_success") is True)
            rate = f"{n_ok/len(sub)*100:.1f}%" if sub else "N/A"
            print(f"    {dt:6s}  n={len(sub)}  ok={n_ok}  rate={rate}")
        print("  By kernel_class × dtype:")
        for kc in kernel_classes:
            for dt in dtypes:
                sub = [r for r in ev if r.get("kernel_class") == kc and r.get("dtype") == dt]
                n_ok = sum(1 for r in sub if r.get("compile_success") is True)
                rate = f"{n_ok/len(sub)*100:.1f}%" if sub else "N/A"
                print(f"    {kc:15s} × {dt:6s}  n={len(sub)}  ok={n_ok}  rate={rate}")
        import collections
        fc = collections.Counter(
            r.get("failure_code") for r in rows if r.get("compile_success") is not None
        )
        if fc:
            print("  failure_code distribution:")
            for code, cnt in fc.most_common():
                print(f"    {str(code):25s}  count={cnt}  ({cnt/len(rows)*100:.1f}%)")


@app.local_entrypoint()
def main(force: bool = False, model: str = "all") -> None:
    targets = {k: v for k, v in FILES.items() if model == "all" or k == model}
    all_rows: dict[str, list[dict]] = {}

    for name, path in targets.items():
        if not path.exists():
            print(f"[skip] {path} not found")
            continue

        rows = _load(path)
        all_rows[name] = rows
        needs_eval = [
            (i, r) for i, r in enumerate(rows)
            if force or r.get("compile_success") is None
        ]
        print(f"\n{path.name}  ({len(rows)} rows, {len(needs_eval)} need evaluation)")
        if not needs_eval:
            print("  All rows already evaluated.")
            continue

        modified = False
        for idx, (row_idx, row) in enumerate(needs_eval):
            source = row.get("source", "")
            kernel_class = row.get("kernel_class", "")
            kernel_name = row.get("kernel_name", "")
            cell = f"{kernel_name} × {row.get('dtype')} × seed={row.get('generation_seed')}"
            print(f"  [{idx+1}/{len(needs_eval)}] {cell}", end=" ", flush=True)

            if not source:
                result = {
                    "compile_success": False,
                    "compile_error_type": "SignatureError",
                    "compile_error_msg": "empty source",
                    "failure_code": "F0_PARSE",
                    "compile_results_by_dtype": {},
                    "n_shapes_tested": 0,
                }
            else:
                try:
                    r = check_compiles_modal(
                        source=source,
                        kernel_class=kernel_class,
                        kernel_name=kernel_name,
                        factor_cell="none",
                        run_id=str(uuid.uuid4()),
                    )
                    result = {
                        "compile_success": r.compile_success,
                        "compile_error_type": r.compile_error_type,
                        "compile_error_msg": r.compile_error_msg,
                        "failure_code": r.failure_code,
                        "compile_results_by_dtype": r.compile_results_by_dtype,
                        "n_shapes_tested": r.n_shapes_tested,
                    }
                except Exception as exc:
                    print(f"→ MODAL ERROR: {exc}")
                    result = {
                        "compile_success": False,
                        "compile_error_type": "UnknownError",
                        "compile_error_msg": str(exc)[:500],
                        "failure_code": "F1_RUNTIME",
                        "compile_results_by_dtype": {},
                        "n_shapes_tested": 0,
                    }

            status = "OK" if result["compile_success"] else result.get("failure_code", "FAIL")
            print(f"→ {status}")
            rows[row_idx].update(result)
            modified = True

        if modified:
            _write(path, rows)
            print(f"  Updated {path}")

    _print_summary(all_rows)
